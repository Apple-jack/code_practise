'''
You are given an integer array sorted in ascending order (may contain duplicates), you need to split them into several
 subsequences, where each subsequences consist of at least 3 consecutive integers. Return whether you can make such a
  split.

Input: [1,2,3,3,4,5]
Output: True
Explanation:
You can split them into two consecutive subsequences :
1, 2, 3
3, 4, 5

Input: [1,2,3,4,4,5]
Output: False
'''

import collections



class Solution:
    def isPossible(self, nums):
        """
        :type nums: List[int]
        :rtype: bool
        """
        counts = collections.Counter(nums)
        tails = collections.Counter()

        for n in nums:
            if counts[n] == 0:
                # 说明所有的数字n都已经入链
                continue
            elif tails[n] > 0:
                # 结尾为n-1的链的数量大于0
                tails[n] -= 1
                tails[n + 1] += 1
            elif counts[n + 1] > 0 and counts[n + 2] > 0:
                # 无链可用，尝试新建链，需要保证新建的链至少有3个数字
                counts[n + 1] -= 1
                counts[n + 2] -= 1
                tails[n + 3] += 1
            else:
                # 不满足以上两种对n的处理方式，说明n无法处理
                return False
            counts[n] -= 1
        return True

